    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        if(tidak_kosong()){
            try {
                s = conn.createStatement();
                String sql = "INSERT INTO buku VALUES('"+ kode_buku.getText() +"','"+ judul.getText() +"','"+ penerbit.getText() +"','"+ tahun.getText() +"', '"+ ckategori.getSelectedItem() +"','"+ jumlah.getText() +"')";
                s.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Data Buku Berhasil DiTambahkan");
                reset();
                tampildata();
                autokodebuku();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan");
                reset();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Harap Lengkapi Data !","Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tambahActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        if(!kode_buku.getText().equals("")){
            try {
                int jawab;
                if((jawab = JOptionPane.showConfirmDialog(null,"Hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION)) == 0){
                    s = conn.createStatement();
                    String sql = "DELETE FROM buku WHERE id_buku='"+ kode_buku.getText() +"'";
                    s.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null,"Data Buku Berhasil di hapus");
                    reset();
                    tampildata();
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan");
                reset();
            }
        }else{
            JOptionPane.showMessageDialog(null,"Pilih ID untuk data yang ingin di hapus!");
        }

    }//GEN-LAST:event_hapusActionPerformed

