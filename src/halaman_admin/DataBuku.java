       public void autokodebuku(){
         try{
            String sql;
            sql = "SELECT COUNT(id_buku) as jumlah FROM buku";
            s = conn.createStatement();
            rs = s.executeQuery(sql);
            if (rs.next()) {
                String jumlah = rs.getString("jumlah");
                int jumint = Integer.parseInt(String.valueOf(jumlah));
                if (jumint > 0) {
                    s = conn.createStatement();
                        sql = "SELECT MAX(id_buku) AS kode FROM buku";
                        rs = s.executeQuery(sql);
                        if(rs.next()){
                            String id = rs.getString("kode").substring(2);
                            String kode = String.valueOf(Integer.parseInt(id) + 1);
                            if(kode.length() == 1){
                               kode_buku.setText("BK00"+kode);
                            }else if(kode.length() == 2){
                                kode_buku.setText("BK00"+kode);
                            }else
                                kode_buku.setText("BK"+kode);
                        }
                }else{
                    kode_buku.setText("BK001");
                }
            }
        }catch(SQLException e){
            
        }
    }
    
