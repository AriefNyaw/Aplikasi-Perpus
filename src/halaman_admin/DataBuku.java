    private void tcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyPressed
        // TODO add your handling code here:
       try {
            s = conn.createStatement();
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            String cr = tcari.getText();
            rs = s.executeQuery("SELECT * FROM buku WHERE id_buku like '%"+ cr +"%'or judul like '%"+ cr +"%'or penerbit like '%"+ cr +"%' or tahun_terbit like '%"+ cr +"%' or kategori like '%"+ cr +"%' or jumlah like '%"+ cr +"%' ORDER BY id_buku DESC " );
             while (rs.next()){
                    Object[] data ={
                     rs.getString("id_buku"),
                     rs.getString("judul"),
                     rs.getString("penerbit"),
                     rs.getString("tahun_terbit"),
                     rs.getString("kategori"),
                     rs.getString("jumlah"),
                    };
                    model.addRow(data);
                    }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_tcariKeyPressed


